    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
        try {
            //Registra JDBC driver
            Class.forName("com.mysql.jdbc.Driver");

            //Abrindo a conexão: ATENÇÃO OS DOIS PARÂMETROS VAZIOS(" ") SÃO USUÁRIO E SENHA, RESPECTIVAMENTE.
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/estoque", "root", "root");

            //Executa a query de inserção
            
            Double preco = parseDouble(txtPreco.getText().replace(",","."));
            
            int qtd = parseInt(txtQtd.getText());
            
            PreparedStatement stmt = conn.prepareStatement("insert into produtos (descricao, preco, quantidade) values(?, ?, ?)");
            stmt.setString(1, txtDescricao.getText());//1 specifies the first parameter in the query
            stmt.setDouble(2, preco);
            stmt.setInt(3, qtd);
            

            int i = stmt.executeUpdate();
            listarProdutos();
            JOptionPane.showMessageDialog(rootPane, "Produto inserido");

        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }//Fim try


    }//GEN-LAST:event_jButtonInserirActionPerformed

