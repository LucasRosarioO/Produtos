    public void listarProdutos(){
        try {
            //Registra JDBC driver
            Class.forName("com.mysql.jdbc.Driver");
            //Abrindo a conexão: ATENÇÃO OS DOIS PARÂMETROS VAZIOS(" ") SÃO USUÁRIO E SENHA, RESPECTIVAMENTE.
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/estoque", "root", "root");
            //Executa a query de seleção
            java.sql.Statement st = conn.createStatement();
            st.executeQuery("select * from produtos");
            ResultSet rs = st.getResultSet();
            //Definir tamanho automático das colunas no modelo
            jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_ALL_COLUMNS);
            //Criando um modelo de acordo com a definição da "JTable1"
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            //Zerar as linhas existentes do modelo
            modelo.setNumRows(0);
            //Lista os produtos no JTable

            
            while (rs.next()) {
                
                Double preco = parseDouble(rs.getString("preco"));
                int qtd = parseInt(rs.getString("quantidade"));
                Double total = preco * qtd;
                DecimalFormat df = new DecimalFormat("0.00");
                
                modelo.addRow(new Object[]{
                    rs.getString("id"),
                    rs.getString("descricao"),
                    preco,
                    qtd,
                    df.format(total)
                });
            }
            
            
            
        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }//Fim try
    
    }
    
    
